package com.soft.entity;

public class Sub_service {

	private String rowno;// 预约服务id
	private String service_id;// 预约服务id
	private String subscriber;// 预约人
	private String matter;// 预约事项
	private String sub_time;// 预约时间
	private String sub_place;// 预约地点
	private String sub_desc;// 预约说明
	private String sub_state;// 状态0成功 1失败 2默认
	private String matter_name;// 状态0成功 1失败 2默认

	public String getService_id() {
		return service_id;
	}

	public void setService_id(String service_id) {
		this.service_id = service_id;
	}

	public String getSubscriber() {
		return subscriber;
	}

	public void setSubscriber(String subscriber) {
		this.subscriber = subscriber;
	}

	public String getMatter() {
		return matter;
	}

	public void setMatter(String matter) {
		this.matter = matter;
	}

	public String getSub_time() {
		return sub_time;
	}

	public void setSub_time(String sub_time) {
		this.sub_time = sub_time;
	}

	public String getSub_place() {
		return sub_place;
	}

	public void setSub_place(String sub_place) {
		this.sub_place = sub_place;
	}

	public String getSub_desc() {
		return sub_desc;
	}

	public void setSub_desc(String sub_desc) {
		this.sub_desc = sub_desc;
	}

	public String getSub_state() {
		return sub_state;
	}

	public void setSub_state(String sub_state) {
		this.sub_state = sub_state;
	}

	
	public String getRowno() {
		return rowno.replace(".0", "");
	}

	public void setRowno(String rowno) {
		this.rowno = rowno;
	}

	
	public String getMatter_name() {
		return matter_name;
	}

	public void setMatter_name(String matter_name) {
		this.matter_name = matter_name;
	}

	@Override
	public String toString() {
		return "Sub_service [rowno=" + rowno + ", service_id=" + service_id + ", subscriber=" + subscriber + ", matter="
				+ matter + ", sub_time=" + sub_time + ", sub_place=" + sub_place + ", sub_desc=" + sub_desc
				+ ", sub_state=" + sub_state + ", matter_name=" + matter_name + "]";
	}

}
